
public class Notas {
    private double valor;
    private Disciplinas nomeDisciplinas;
    
    public void notas (double nota, Disciplinas nomeDisciplinas){
        this.valor = valor;
        this.nomeDisciplinas = nomeDisciplinas;
        
    }
    
    public double getNotas(){
       
        return this.valor;
    
    }
    
    public Disciplinas getDisciplinas(){
       
        return this.nomeDisciplinas;
    
    }
    
    public void setNotas(double valor){
        
        this.valor = valor;
    
    }
    
}
    

